#include <iostream>
#include <conio.h>

using namespace std;

int main()
{
    int x = 10;
    int y = 16;
    cout << "Nilai x                            :"; cin >> x;
    cout << "Nilai y                            :"; cin >> y;
    cout << "Hasil 1 (x & y)                    : " << (x & y) << endl;
    cout << "Hasil 2 (x | y)                    : " << (x | y) << endl;
    cout << "Hasil 3 (x >> 2)                   : " << (x >> 2) << endl;
    cout << "Hasil 4 (x << 1)                   : " << (x << 1) << endl;
    cout << "Hasil 5 (y << 1)                   : " << (y << 1) << endl;
    getch ();
}
